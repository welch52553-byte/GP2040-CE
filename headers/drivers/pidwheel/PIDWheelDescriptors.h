// =====================================================
// Report Structures
// =====================================================
typedef struct __attribute__((packed, aligned(1))) {
    uint8_t  report_id;
    uint32_t buttons;
    int16_t  X;
    int16_t  Y;
    int16_t  Z;
    int16_t  RX;
    int16_t  RY;
    int16_t  RZ;
    int16_t  Dial;
    int16_t  Slider;
} PIDWheelInputReport;

typedef struct __attribute__((packed, aligned(1))) {
    uint8_t report_id;
    uint8_t status;
} PIDWheelStateReport;

typedef struct {
    int16_t cpOffset;
    int16_t positiveCoefficient;
    int16_t negativeCoefficient;
    uint16_t positiveSaturation;
    uint16_t negativeSaturation;
    uint16_t deadBand;
} PIDEffectCondition;

typedef struct {
    volatile uint8_t state;
    uint8_t type;
    int16_t offset;
    uint8_t gain;
    int16_t magnitude;
    int16_t startLevel;
    int16_t endLevel;
    PIDEffectCondition condition;
    int16_t phase;
    uint16_t period;
    uint32_t duration;
    uint16_t attackLevel;
    uint16_t fadeLevel;
    uint32_t attackTime;
    uint32_t fadeTime;
    uint16_t startDelay;
    uint32_t startTime;
    bool useEnvelope;
} PIDEffect;

// Report parsing structs (match descriptor layout, no report_id)
typedef struct __attribute__((packed, aligned(1))) {
    uint8_t  effectBlockIndex;
    uint8_t  effectType;
    uint16_t duration;
    uint16_t triggerRepeatInterval;
    uint16_t samplePeriod;
    uint16_t startDelay;
    uint8_t  gain;
    uint8_t  triggerButton;
    uint8_t  enableAxis;
    uint16_t directionX;
    uint16_t typeSpecificBlockOffset;
} PIDWheelSetEffect;

typedef struct __attribute__((packed, aligned(1))) {
    uint8_t  effectBlockIndex;
    uint16_t attackLevel;
    uint16_t fadeLevel;
    uint32_t attackTime;
    uint32_t fadeTime;
} PIDWheelSetEnvelope;

typedef struct __attribute__((packed, aligned(1))) {
    uint8_t  effectBlockIndex;
    uint8_t  parameterBlockOffset;
    int16_t  cpOffset;
    int16_t  positiveCoefficient;
    int16_t  negativeCoefficient;
    uint16_t positiveSaturation;
    uint16_t negativeSaturation;
    uint16_t deadBand;
} PIDWheelSetCondition;

typedef struct __attribute__((packed, aligned(1))) {
    uint8_t  effectBlockIndex;
    uint16_t magnitude;
    int16_t  offset;
    uint16_t phase;
    uint32_t period;
} PIDWheelSetPeriodic;

typedef struct __attribute__((packed, aligned(1))) {
    uint8_t  effectBlockIndex;
    int16_t  magnitude;
} PIDWheelSetConstantForce;

typedef struct __attribute__((packed, aligned(1))) {
    uint8_t effectBlockIndex;
    int16_t startLevel;
    int16_t endLevel;
} PIDWheelSetRamp;

typedef struct __attribute__((packed, aligned(1))) {
    uint8_t effectBlockIndex;
    uint8_t operation;
    uint8_t loopCount;
} PIDWheelEffectOperation;

// Feature report structs
typedef struct __attribute__((packed, aligned(1))) {
    uint8_t  effectBlockIndex;
    uint8_t  loadStatus;
    uint16_t ramPoolAvailable;
} PIDWheelBlockLoad;

typedef struct __attribute__((packed, aligned(1))) {
    uint16_t ramPoolSize;
    uint8_t  maxSimultaneousEffects;
    uint8_t  memoryManagement;
} PIDWheelPoolReport;

